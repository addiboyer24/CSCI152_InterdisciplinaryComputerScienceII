public class Department{ // Class definition.
    // Instance variables.
    private String name;

    private String building;

    private long numberOfStudents;

    // Constructors.
    public Department(){} // Default constructor.

    public Department(String name, String building, long numberOfStudents){
        this.name = name;
        this.building = building;
        this.numberOfStudents = numberOfStudents;
    }

    // Instance methods.
}